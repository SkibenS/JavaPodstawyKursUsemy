public class TypyDanych {

    // liczby stałoprzecinkowe
    byte aByte;      // -128    do  127
    short aShort;    // -32768  do  32767
    int anInt;       // -2147483648 do 2147483647
    long aLong;      // -2^63 do 2^63

    // liczby zmienno-przecinkowe
    float aFloat;   // max 7 miejsc po przecinku
    double aDouble; // max 15 miejsc po przecinku

    // char - prezentuje pojedynczy znak
    char aChar;     // jeden znak

    //boolean
    boolean aBoolean; // true albo false

    // znaki specialne w JAVA'ie
    // t - tab
    // \n - nowa linia
    // \" - cudzysłów
}


